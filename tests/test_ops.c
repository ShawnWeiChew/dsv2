bool check_arrays(float *a, float *b, size_t length) {
    for (int i = 0; i < length; i++) {
        float diff = a[i] - b[i];
        if ((diff < 0 ? -diff : diff) >= 1E-4) {
            fprintf(stderr, "\n====================\n");
            fprintf(stderr, "Element %d was not equal. Expected %f but got %f", i, b[i], a[i]);
            fprintf(stderr, "\n====================\n");
            return false;
        }
    }

    return true;
}

